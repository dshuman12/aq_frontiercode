def get_baseline_qx(age: int, sex: str) -> list[float]:
    """Return the q(x) table starting at the given age."""
    table = SSA_QX_MALE if sex == "male" else SSA_QX_FEMALE
    return table[age:] if age < len(table) else [1.0]


def get_baseline_life_expectancy(age: int, sex: str) -> float:
    """Return SSA baseline remaining life expectancy at a given age."""
    table = SSA_EX_MALE if sex == "male" else SSA_EX_FEMALE
    if age < len(table):
        return table[age]
    return 0.5


def life_expectancy_from_qx(qx_curve: list[float], start_age: int) -> float:
    """Compute life expectancy from a q(x) curve using the standard actuarial method."""
    lx = 100_000.0
    tx = 0.0
    for qx in qx_curve:
        qx = min(qx, 1.0)
        dx = lx * qx
        lx_mid = lx
        tx += lx_mid
        lx -= dx
        if lx < 0.5:
            break
    return tx / 100_000.0


def get_survival_curve(qx_curve: list[float], start_age: int) -> list[tuple[int, float]]:
    """Compute survival probabilities as a list of (age, survival_prob) tuples."""
    survival = 1.0
    curve: list[tuple[int, float]] = [(start_age, 1.0)]
    for i, qx in enumerate(qx_curve):
        survival *= (1.0 - min(qx, 1.0))
        curve.append((start_age + i + 1, round(survival, 6)))
        if survival < 0.001:
            break
    return curve
